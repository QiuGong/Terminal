static int cJSON_strcasecmp(const char *s1,const char *s2)
{
	if (!s1) return (s1==s2)?0:1;if (!s2) return 1;
	for(; toLower(*s1) == toLower(*s2); ++s1, ++s2)	if(*s1 == 0)	return 0;
	return toLower(*(const unsigned char *)s1) - toLower(*(const unsigned char *)s2);
}

static void *(*cJSON_malloc)(rt_size_t sz) = rt_malloc;
static void (*cJSON_free)(void *ptr) = rt_free;

static char* cJSON_strdup(const char* str)
{
      rt_size_t len;
      char* copy;

      len = rt_strlen(str) + 1;
      if ((copy = (char*)cJSON_malloc(len)) == 0) return 0;
      rt_memcpy(copy,str,len);
      return copy;
}

void cJSON_InitHooks(cJSON_Hooks* hooks)
{
    if (!hooks) { /* Reset hooks */
        cJSON_malloc = rt_malloc;
        cJSON_free = rt_free;
        return;
    }

	cJSON_malloc = (hooks->malloc_fn)?hooks->malloc_fn:rt_malloc;
	cJSON_free	 = (hooks->free_fn)?hooks->free_fn:rt_free;
}

/* Internal constructor. */
static cJSON *cJSON_New_Item()
{
	cJSON* node = (cJSON*)cJSON_malloc(sizeof(cJSON));
	if (node) rt_memset(node,0,sizeof(cJSON));
	return node;
}

/* Delete a cJSON structure. */
void cJSON_Delete(cJSON *c)
{
	cJSON *next;
	while (c)
	{
		next=c->next;
		if (!(c->type&cJSON_IsReference) && c->child) cJSON_Delete(c->child);
		if (!(c->type&cJSON_IsReference) && c->valuestring) cJSON_free(c->valuestring);
		if (c->string) cJSON_free(c->string);
		cJSON_free(c);
		c=next;
	}
}

/* Render the cstring provided to an escaped version that can be printed. */
static char *print_string_ptr(const char *str)
{
	const char *ptr;char *ptr2,*out;int len=0;unsigned char token;
	
	if (!str) return cJSON_strdup("");
	ptr=str;
	while (((token=*ptr) != 0) && ++len) {
	    if (0 == strChr("\"\\\b\f\n\r\t",token)) 
		    len++; 
		else if (token<32) 
		    len+=5;
		ptr++;
	}
	
	out=(char*)cJSON_malloc(len+3);
	if (!out) return 0;

	ptr2=out;ptr=str;
	*ptr2++='\"';
	while (*ptr)
	{
		if ((unsigned char)*ptr>31 && *ptr!='\"' && *ptr!='\\') *ptr2++=*ptr++;
		else
		{
			*ptr2++='\\';
			switch (token=*ptr++)
			{
				case '\\':	*ptr2++='\\';	break;
				case '\"':	*ptr2++='\"';	break;
				case '\b':	*ptr2++='b';	break;
				case '\f':	*ptr2++='f';	break;
				case '\n':	*ptr2++='n';	break;
				case '\r':	*ptr2++='r';	break;
				case '\t':	*ptr2++='t';	break;
				default: rt_sprintf(ptr2,"u%04x",token);ptr2+=5;	break;	/* escape and print */
			}
		}
	}
	*ptr2++='\"';*ptr2++=0;
	return out;
}
/* Invote print_string_ptr (which is useful) on an item. */
static char *print_string(cJSON *item)	{return print_string_ptr(item->valuestring);}

